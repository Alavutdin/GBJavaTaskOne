package Task2.Seminal1.Lesson3;

public class Product {
    private String name = "coffe";
    private Long id;

    public Product(String name, Long id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }
}
